<template>
  <search>
    <template #title>
      <title-card
        :title="$t('common.analysesCount')"
        :subtitle="$t('common.count', { count: count })"
        icon="mdi-chart-scatter-plot"
        class="title-analysis"
      />
    </template>

    <template #form>
      <analysis-search-form />
      <search-view-map-wrapper
        :items="items"
        class="mb-6 mt-2"
        @update="handleUpdate"
      />
    </template>

    <template #result>
      <analysis-table
        :show-search="false"
        :items="items"
        :count="count"
        :options="options"
        use-dynamic-headers
        @update="handleUpdate"
      />
    </template>
  </search>
</template>

<script>
import { mapState, mapActions } from 'vuex'
import AnalysisTable from '@/components/tables/AnalysisTable'
import AnalysisSearchForm from '~/components/search/forms/AnalysisSearchForm.vue'
import SearchViewMapWrapper from '~/components/map/SearchViewMapWrapper'
import Search from '~/components/templates/Search'
import dynamicTableHeaders from '~/mixins/dynamicTableHeaders'
import TitleCard from '~/components/TitleCard.vue'

export default {
  name: 'AnalysisSearch',
  components: {
    Search,
    SearchViewMapWrapper,
    AnalysisSearchForm,
    AnalysisTable,
    TitleCard,
  },
  mixins: [dynamicTableHeaders],
  head() {
    return {
      title: this.$t('analysis.pageTitle'),
      meta: [
        {
          property: 'og:title',
          content: this.$t('analysis.pageTitle'),
          hid: 'og:title',
        },
      ],
    }
  },
  computed: {
    ...mapState('landing', ['search']),
    ...mapState('analysis', ['items', 'count', 'options']),
  },
  methods: {
    ...mapActions('analysis', ['searchAnalyses']),
    async handleUpdate(tableState) {
      await this.searchAnalyses(tableState?.options)
    },
  },
}
</script>
